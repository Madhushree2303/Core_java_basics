package Java_basics_02;
public class Java_concept_14 {
    public static void main(String[] args) {
        // increment operator

        int a = 20;
        System.out.println(a++); // post increment
        System.out.println(a);
        int b=30;
        System.out.println(++b); // pre increment
        System.out.println(b);
    }
}

