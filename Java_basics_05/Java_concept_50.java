package Java_basics_05;

public class Java_concept_50 {
    public static void main(String[] args) {
        // write a program for sum of digit


         int num=10;

         int sum=0;

         for (int i=1; i<=num; i++){
             sum=sum+i;
         }
        System.out.println("Sum of 10 is -> " + sum);

    }
}

