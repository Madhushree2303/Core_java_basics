package Java_basics_04;
public class Java_concept_38 {
    public static void main(String[] args) {
        //Print the Even number between 1 to 50

        for (int i = 0; i<=50; i++) {
            if(i%2==0){
                System.out.println(i+ " is Even");

            }
        }
        System.out.println(" --- End of Program!! --- ");

    }
}

