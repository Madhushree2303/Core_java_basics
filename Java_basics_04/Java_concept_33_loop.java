package Java_basics_04;
public class Java_concept_33_loop {
    public static void main(String[] args) {

        // for( A ;B ; C)
        // A -> Initialization -> JVM What variable, where you are starting point
        // B - Condition -> When you want this to stop.
        // C ->  Increment / Decrement


        for (int i=1; i<10; i++ ){
            System.out.println("Print Madhu 9 times");
        }

        for (int j=0; j<=10; j++){
            System.out.println("print balan 10 times");
        }
        System.exit(137);
    }
}

